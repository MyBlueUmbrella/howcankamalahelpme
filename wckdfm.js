function displayContent() {
    var conditionKey = "";
    var resultContent = "";
    ["age", "home", "parent", "work"].forEach((category) => {
        document.querySelectorAll('input[name="' + category + '"]').forEach(cb => {
            if (cb.checked) {
                conditionKey += cb.id
                resultContent += conditionsDict[cb.id].kamala + "<br>" + conditionsDict[cb.id].trump + "<br>";
            }
        });
    });
    
    // TODO: bring back in for top combo
    // const resultContent = conditionsDict[conditionKey];
    const resultDiv = document.getElementById('result');
    resultDiv.innerHTML = resultContent;
    resultDiv.style.display = 'block';
    resultDiv.scrollIntoView({ behavior: 'smooth' });
}

function makeExclusive(checkbox) {
  const checkboxes = document.querySelectorAll('input[name=' + checkbox.name + ']');
  checkboxes.forEach(cb => {
    if (cb !== checkbox) {
      cb.checked = false;
    }
  });
}
